// 材料
let 純ココア = 2; // 小さじ2
let 砂糖 = 2; // 小さじ2、またははちみつを使う場合、砂糖の0.75倍
let はちみつ = 砂糖 * 0.75; // 小さじ1.5
let 牛乳_練る用 = 10; // ml
let 牛乳_全量 = 150; // ml

// ココアクラスを定義
class Cocoa {
  #名前;
  #純ココア;
  #砂糖;
  #はちみつ;
  #牛乳_練る用;
  #牛乳_全量;
  #冷たいココアが飲みたい;

  constructor(名前, 純ココア, 砂糖, はちみつ, 牛乳_練る用, 牛乳_全量, 冷たいココアが飲みたい) {
    this.#名前 = 名前;
    this.#純ココア = 純ココア;
    this.#砂糖 = 砂糖;
    this.#はちみつ = はちみつ;
    this.#牛乳_練る用 = 牛乳_練る用;
    this.#牛乳_全量 = 牛乳_全量;
    this.#冷たいココアが飲みたい = 冷たいココアが飲みたい;
  }

  ココアを煎る() {
    let 幸せに感じる匂いがする = false;
    console.log('弱火でココアを煎り始めます...');
    while (!幸せに感じる匂いがする) {
      console.log('ココアの香りが広がってきました...');
      // 幸せに感じる匂いがする条件を満たしたら true になると仮定
      幸せに感じる匂いがする = true;
    }
    console.log('純ココアの色が濃くなり、香ばしい匂いがしてきたので火を止めます。');
  }

  ペーストを作る() {
    let ペーストが滑らか = false;
    while (!ペーストが滑らか) {
      console.log('牛乳 ' + this.#牛乳_練る用 + 'ml を加えて、滑らかになるまで練っています...');
      // 条件を満たしたらペーストが滑らかになると仮定
      ペーストが滑らか = true;
    }
    console.log('照りのあるペースト状になりました。');
  }

  砂糖と牛乳を加える() {
    let 使用する甘味料 = '砂糖';
    if (使用する甘味料 === '砂糖') {
      console.log('砂糖を小さじ ' + this.#砂糖 + ' 加えます。');
    } else if (使用する甘味料 === 'はちみつ') {
      console.log('はちみつを小さじ ' + this.#はちみつ + ' 加えます。');
    }
    console.log('さらに牛乳 ' + this.#牛乳_全量 + 'ml を加えてよく混ぜます。');
  }

  ココアを温める() {
    let 温度 = 20; // 初期温度 (室温)
    while (温度 < 90) { // 90度手前まで温める
      温度 += 10;
      console.log('現在の温度は ' + 温度 + ' 度です。');
    }
    console.log('沸騰寸前で火を止め、マグカップに注いだら完成です。');
  }

  ココアを冷やす() {
    console.log('ココアを冷蔵庫に入れて冷やしています...');
    console.log('冷たいココアが飲みたい場合は、しばらく冷蔵庫で冷やしてからお楽しみください。');
  }

  ココアを作る() {
    this.ココアを煎る();
    this.ペーストを作る();
    this.砂糖と牛乳を加える();
    this.ココアを温める();
    if (this.#冷たいココアが飲みたい) {
      this.ココアを冷やす();
    }
  }
}

// ココアのインスタンスを作成して作成手順を実行
let おいしいココア = new Cocoa('おいしいココア', 純ココア, 砂糖, はちみつ, 牛乳_練る用, 牛乳_全量, true);
おいしいココア.ココアを作る();