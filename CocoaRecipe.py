# 材料
純ココア = 2  # 小さじ2
砂糖 = 2  # 小さじ2、またははちみつを使う場合、砂糖の0.75倍
はちみつ = 砂糖 * 0.75  # 小さじ1.5
牛乳_練る用 = 10  # ml
牛乳_全量 = 150  # ml

class Cocoa:
    def __init__(self, 名前, 純ココア, 砂糖, はちみつ, 牛乳_練る用, 牛乳_全量, 冷たいココアが飲みたい):
        self.名前 = 名前
        self.純ココア = 純ココア
        self.砂糖 = 砂糖
        self.はちみつ = はちみつ
        self.牛乳_練る用 = 牛乳_練る用
        self.牛乳_全量 = 牛乳_全量
        self.冷たいココアが飲みたい = 冷たいココアが飲みたい

    def ココアを煎る(self):
        幸せに感じる匂いがする = False
        print('弱火でココアを煎り始めます...')
        while not 幸せに感じる匂いがする:
            print('ココアの香りが広がってきました...')
            # 幸せに感じる匂いがする条件を満たしたら True になると仮定
            幸せに感じる匂いがする = True
        print('純ココアの色が濃くなり、香ばしい匂いがしてきたので火を止めます。')

    def ペーストを作る(self):
        ペーストが滑らか = False
        while not ペーストが滑らか:
            print(f'牛乳 {self.牛乳_練る用}ml を加えて、滑らかになるまで練っています...')
            # 条件を満たしたらペーストが滑らかになると仮定
            ペーストが滑らか = True
        print('照りのあるペースト状になりました。')

    def 砂糖と牛乳を加える(self):
        使用する甘味料 = '砂糖'
        if 使用する甘味料 == '砂糖':
            print(f'砂糖を小さじ {self.砂糖} 加えます。')
        elif 使用する甘味料 == 'はちみつ':
            print(f'はちみつを小さじ {self.はちみつ} 加えます。')
        print(f'さらに牛乳 {self.牛乳_全量}ml を加えてよく混ぜます。')

    def ココアを温める(self):
        温度 = 20  # 初期温度 (室温)
        while 温度 < 90:  # 90度手前まで温める
            温度 += 10
            print(f'現在の温度は {温度} 度です。')
        print('沸騰寸前で火を止め、マグカップに注いだら完成です。')

    def ココアを冷やす(self):
        print('ココアを冷蔵庫に入れて冷やしています...')
        print('冷たいココアが飲みたい場合は、しばらく冷蔵庫で冷やしてからお楽しみください。')

    def ココアを作る(self):
        self.ココアを煎る()
        self.ペーストを作る()
        self.砂糖と牛乳を加える()
        self.ココアを温める()
        if self.冷たいココアが飲みたい:
            self.ココアを冷やす()

# ココアのインスタンスを作成して作成手順を実行
おいしいココア = Cocoa('おいしいココア', 純ココア, 砂糖, はちみつ, 牛乳_練る用, 牛乳_全量, True)
おいしいココア.ココアを作る()
