// 材料
let 純ココア = 2; // 小さじ2
let 砂糖 = 2; // 小さじ2、またははちみつを使う場合、砂糖の0.75倍
let はちみつ = 砂糖 * 0.75; // 小さじ1.5
let 牛乳_練る用 = 10; // ml
let 牛乳_全量 = 150; // ml

// ココアを軽く煎る
// ココアを煎ることで、香ばしさが増しますが焦げないように注意してください
function ココアを煎る() {
  let 幸せに感じる匂いがする = false;
  console.log('弱火でココアを煎っています...');
  while (!幸せに感じる匂いがする) {
    console.log('純ココアを煎っています...');
    // 幸せに感じる匂いがする条件を満たしたら true になると仮定
    幸せに感じる匂いがする = true;
  }
  console.log('純ココアの色が濃くなり、香ばしい匂いがしてきたら火を止めます。');
}

// ペーストを作る
// 少量の牛乳を加えて滑らかにすることがポイントです。ダマにならないようにしっかり練りましょう
function ペーストを作る() {
  let ペーストが滑らか = false;
  while (!ペーストが滑らか) {
    console.log('牛乳 ' + 牛乳_練る用 + 'ml を加えて練っています...');
    // 条件を満たしたらペーストが滑らかになると仮定
    ペーストが滑らか = true;
  }
  console.log('照りのあるペースト状になりました。');
}

// 砂糖と牛乳を加えて混ぜる
// 砂糖やはちみつは好みに合わせて調整してください
function 砂糖と牛乳を加える() {
  let 使用する甘味料 = '砂糖';
  if (使用する甘味料 === '砂糖') {
    console.log('砂糖を小さじ ' + 砂糖 + ' 加えます。');
  } else if (使用する甘味料 === 'はちみつ') {
    console.log('はちみつを小さじ ' + はちみつ + ' 加えます。');
  }
  console.log('さらに牛乳 ' + 牛乳_全量 + 'ml を加えてよく混ぜ合わせます。');
}

// ココアを温める
// 弱火でじっくり温めるのがコツです。沸騰させないように注意しましょう
function ココアを温める() {
  let 温度 = 20; // 初期温度 (室温)
  while (温度 < 90) { // 90度手前まで温める
    温度 += 10;
    console.log('現在の温度: ' + 温度 + '度');
  }
  console.log('沸騰寸前で火を止めて、マグカップに注いだらできあがりです。');
}

// ココアを冷やす
// 冷たいココアが飲みたい場合、冷蔵庫で冷やします
function ココアを冷やす() {
  console.log('ココアを冷蔵庫に入れて冷やします...');
  console.log('冷たいココアが飲みたい場合は、しばらく冷蔵庫で冷やしてからお楽しみください。');
}

// ココア作成の手順
function ココアを作る() {
  ココアを煎る();
  ペーストを作る();
  砂糖と牛乳を加える();
  ココアを温める();
  let 冷たいココアが飲みたい = true; // 冷たいココアが飲みたい場合は true に設定
  if (冷たいココアが飲みたい) {
    ココアを冷やす();
  }
}

// ココアを作成します
ココアを作る();
